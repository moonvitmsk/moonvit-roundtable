export function buildFullPrompt(config: PromptConfig): string {
  const parts = [config.companyContext];
  if (config.goals) parts.push(config.goals);
  if (config.taskType) parts.push(config.taskType);
  if (config.customInstructions) parts.push(`\n## Дополнительные инструкции\n${config.customInstructions}`);
  return parts.join("\n\n");
}
